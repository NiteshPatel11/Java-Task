public class    DataType {
    public static void main(String[] args){

        // Data Type

        int  i = 0; // interger type
        float f = 0.0f; // integre with point value
        double d = 0.0657d; // large point value
        char c = 'a'; // char type
        String str = "abc";
        System.out.println("This is int type - "+i);
        System.out.println("This is float type - "+f);
        System.out.println("This is double type - "+d);
        System.out.println("This is char type - "+c);
        System.out.println("This is String type - "+ str );
    }

}
